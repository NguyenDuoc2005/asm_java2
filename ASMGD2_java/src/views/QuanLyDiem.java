    public boolean checkTimTheoMa() {
        if (txtTimMa.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập mã!");
            return false;
        }
        return true;
    }

    public void Search() {
        String Timma = txtTimMa.getText();
        int kt = 0;
        for (Grade gr : qld.listD) {
            if (Timma.equals(gr.getMaSV())) {
                lblHoTen.setText(gr.getHoTen());
                txtMaSV.setText(gr.getMaSV());
                txtTiengAnh.setText(gr.getTiengAnh() + "");
                txtTinHoc.setText(gr.getTinHoc() + "");
                txtGiaoDucTC.setText(gr.getGDTC() + "");
                lblDiemTB.setText(gr.getDiemTB() + "");
                kt = 1;
                break;
            }
        }
        if (kt == 0) {
            JOptionPane.showMessageDialog(this, "Không tìm thấy SV có mã: " + txtTimMa.getText());
            newSV();
        }
    }

