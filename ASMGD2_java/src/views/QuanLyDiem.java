    public boolean validate_() {
//        if(txtMaSV.getText().isEmpty()){
//            JOptionPane.showMessageDialog(this,"Tìm kiếm hoặc chọn bản ghi trước khi ADD!");
//                    return false; 
//        }
        if (txtTiengAnh.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Chưa nhập điểm TA");
            return false;
        }
        if (txtGiaoDucTC.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Chưa nhập điểm TA");
            return false;
        }
        if (txtTinHoc.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Chưa nhập điểm TA");
            return false;
        }
        int ta, gdtc, tin;
        try {
            ta = Integer.parseInt(txtTiengAnh.getText());
            gdtc = Integer.parseInt(txtGiaoDucTC.getText());
            tin = Integer.parseInt(txtTinHoc.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Điểm phải nhập số nguyên");
            return false;
        }

        if (ta > 10 || ta < 0 || gdtc > 10 || gdtc < 0 || tin > 10 || tin < 0) {
            JOptionPane.showMessageDialog(this, "Điểm phải từ (0-->10)");
            return false;
        }

        return true;
    }

    public Grade addDiem() {
        int tiengAnh, tinHoc, GDTC;
        String ma;
        tiengAnh = Integer.parseInt(txtTiengAnh.getText());
        GDTC = Integer.parseInt(txtGiaoDucTC.getText());
        tinHoc = Integer.parseInt(txtTinHoc.getText());
        ma = txtMaSV.getText();
        Grade gr = new Grade(ma, tiengAnh, tinHoc, GDTC);
        return gr;
    }

